//  Comparison Operator

let num1 = 8;
let num2 = 7;
let num3 = '8';

console.log(`Using '>' Operator : ${num1 > num2}`);
console.log(`Using '>=' Operator : ${num1 >= num2}`);
console.log(`Using '<' Operator : ${num1 < num2}`);
console.log(`Using '<=' Operator : ${num1 <= num2}`);
console.log(`Using '==' Operator : ${num1 == num3}`);
console.log(`Using '===' Operator : ${num1 === num3}`);