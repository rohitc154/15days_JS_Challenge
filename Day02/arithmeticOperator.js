let num1 = 64;
let num2 = 7;

//  Adding Two Number
function addNum(num1, num2) {
    return num1 + num2;
}
console.log(`Adding Numbers : ${addNum(num1, num2)}`);

// Subtracting Two Number
function subNum(num1, num2) {
    return num1 - num2;
}
console.log(`Subtracting Numbers : ${subNum(num1, num2)}`);

// Multiplying Two Number
function mulNum(num1, num2) {
    return num1 * num2;
}
console.log(`Multiplying Numbers : ${mulNum(num1, num2)}`);

//Dividing Two Number
function divNum(num1, num2) {
    return num1 / num2;
}
console.log(`Dividing Numbers : ${divNum(num1, num2)}`); // It gives the quotient after dividing

//Modulus Operator
function divNum(num1, num2) {
    return num1 % num2;
}
console.log(`Modulus Operator : ${divNum(num1, num2)}`); // It gives the remainder after dividing