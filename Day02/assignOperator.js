// Assignment Operator

let num = 45;
console.log(`Initial Value : ${num}`);

num += 10; // Reassigning the value to the 'num' variable.
console.log(`Reassigning Value using '+=' Operator : ${num}`);

num -= 15; // Reassigning the value to the 'num' variable.
console.log(`Reassigning Value using '-=' Operator : ${num}`); 